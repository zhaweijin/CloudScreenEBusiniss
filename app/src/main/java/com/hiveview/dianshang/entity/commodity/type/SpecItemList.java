public class SpecItemList {
    private String color;
    private String name;
    private String specItemSn;
    private String specSn;
    private String type;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSpecItemSn() {
        return specItemSn;
    }

    public void setSpecItemSn(String specItemSn) {
        this.specItemSn = specItemSn;
    }

    public String getSpecSn() {
        return specSn;
    }

    public void setSpecSn(String specSn) {
        this.specSn = specSn;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
